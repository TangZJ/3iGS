        N_voxel_init = N_voxel_init ** 3
        reso_cur = N_to_reso(N_voxel_init, aabb)

        self.lighting = TensorVMSplit(
            n_voxels=N_voxel_init,
            aabb=aabb,
            gridSize=reso_cur,
            device="cuda",
            density_n_comp = 8, appearance_n_comp = [48,48,48], app_dim = 48,
                    shadingMode = 'MLP_PE', alphaMask = None, near_far=[2.0,6.0],
                    density_shift = -10, alphaMask_thres=0.001, distance_scale=25, rayMarch_weight_thres=0.0001,
                    pos_pe = 6, view_pe = 4, fea_pe = 6, featureC=128, step_ratio=2.0,
                    fea2denseAct = 'softplus'
        )
        
