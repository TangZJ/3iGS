    def shrink(self):
        points = self.get_xyz
        points = points.cpu().numpy()
        if self.blender_bool:
            x_low, x_high = np.min(points[:, 0]) * 1.2, np.max(points[:, 0]) * 1.2
            y_low, y_high = np.min(points[:, 1]) * 1.2 ,np.max(points[:, 1]) * 1.2
            z_low, z_high = np.min(points[:, 2]) * 1.2, np.max(points[:, 2]) * 1.2
        else:
            x_low, x_high = np.percentile(points[:, 0], 2.0) * 1.15, np.percentile(points[:, 0], 98.0) * 1.2
            y_low, y_high = np.percentile(points[:, 1], 2.0) * 1.15 ,np.percentile(points[:, 1], 98.0) * 1.2
            z_low, z_high = np.percentile(points[:, 2], 2.0) * 1.15, np.percentile(points[:, 2], 98.0) * 1.2
        new_aabb = torch.tensor([[x_low, y_low, z_low], [x_high, y_high, z_high]]).to("cuda")

        return self.lighting.shrink(new_aabb)


